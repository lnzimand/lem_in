#include "../lem_in.h"
/*****************************************************************************
*  ----------------------------- input_to_array ---------------------------  *
******************************************************************************/

char    **input_to_array(void)
{
    char    *str;
    char    **arr;

    /***********************************************************************
    *  Get string, delimetered by "'", from stdin.                         *
    ************************************************************************/
    str = read_input();
    /***********************************************************************
    *  Break up a string, delimetered by "'" into an array                 *
    ************************************************************************/
    arr = ft_strsplit(str, '\'');
    ft_strdel(&str);
    return arr;
}