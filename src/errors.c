#include "../lem_in.h"
/*****************************************************************************
*  ----------------------------- errors -----------------------------------  *
******************************************************************************/

int     errors(char *room)
{
    char    *temp;
    char    **temp2;

    /*************************************************************************
    *  Skip the room name                      .                             *
    **************************************************************************/
    temp = ft_strchr((const char*)room, ' ');
    if (temp == NULL)
        return 1;
    /*************************************************************************
    * Check if the coordinates are numbers.                                  *
    **************************************************************************/
    temp2 = ft_strsplit((const char*)temp, ' ');
    if (!all_digits(temp2))
    {
        /*********************************************************************
        *  If the coordinates are not numbers free temp2 and return true.    *
        **********************************************************************/
        free_array(temp2);
        return 1;
    }
    /*************************************************************************
    *  No errors found.                                                      *
    **************************************************************************/
    return 0;
}
