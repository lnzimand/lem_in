/*****************************************************************************
*  ----------------------------- get_links --------------------------------  *
******************************************************************************/

void   get_links(char **arr, List *head)
{
    while (*arr)
    {
        /*****************************************************************************
        *  Store any string with '-'                                                 *
        ******************************************************************************/
        if (ft_strchr(*arr, '-'))
            list_ins_next(head, NULL, *arr);
        arr++;
    }
}